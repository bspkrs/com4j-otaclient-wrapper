package com.hp.alm.otaclient;

import com4j.*;

/**
 * <p>
 * Sort order for filtered results.
 * </p>
 */
public enum TDAPI_FILTERORDER
{
    /**
     * <p>
     * Sort ascending.
     * </p>
     * <p>
     * The value of this constant is 0
     * </p>
     */
    TDOLE_ASCENDING, // 0
    /**
     * <p>
     * Sort descending.
     * </p>
     * <p>
     * The value of this constant is 1
     * </p>
     */
    TDOLE_DESCENDING, // 1
}
